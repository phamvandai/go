package main

import "fmt"

// Go built-in data types
// Number:
// uint8, uint16, uint32, uint64, int8, int16, int32, int64
// float32, float64, complex64, complex128

// String: string

// Boolean: true, false

func main() {
	// Number
	fmt.Println("1 + 1 = ", 1+1)
	fmt.Println("1.1 * 2.2 = ", 1.1*2.2)
	// String
	fmt.Println("Hello go")
	// Boolean
	fmt.Println(" 10 > 2: ", (10 > 2))
}
